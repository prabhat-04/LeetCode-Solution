//User function template for C++
class Solution{
public:
	void pushZerosToEnd(int arr[], int n) {
	    // code here
	    int idx=0;
        while(idx<n-1 && arr[idx]!=0) idx++;
        int ne=idx+1;
        while(ne<n){
            if(arr[ne]!=0){
                int temp=arr[idx];
                arr[idx]=arr[ne];
                arr[ne]=temp;
                idx++;
            }
         ne++;
        }
	}
};

